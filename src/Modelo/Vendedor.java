    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(rut.getText().isEmpty() || nombre.getText().isEmpty()
                 || apellido.getText().isEmpty() || fono.getText().isEmpty()
                 || fecha.getText().isEmpty() || sueldo.getText().isEmpty()){
            
        }else{
            if(Metodo.BuscarVendedor(rut.getText().toString())){
                System.out.println("Vendedor duplicado");
            }else{
                String Nombre_Jefe = jefe.getSelectedItem().toString();
                String JEFE="";
                for (int x = 0; x < lol.length; x++) {
                    if (Nombre_Jefe.equals((String) lol[x][1])) {
                        JEFE=(String) lol[x][0];
                    }
                }
                Metodo.CrudVendedor(1, rut.getText(), 
                        nombre.getText(), apellido.getText(), fono.getText(), 
                        fecha.getText(), Integer.parseInt(sueldo.getText()), JEFE);
                Vista.Vendedor Vendedor = new Vista.Vendedor();
                Main.panel.add(Vendedor);
                Vendedor.show();
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


